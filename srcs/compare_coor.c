#include "../includes/filler.h"


int		compare_coor(int *c1, int *c2)
{
	int dist1;
	int dist2;

	dist1 = c1[2] > c1[3] ? c1[2] : c1[3];
	dist2 = c2[2] > c2[3] ? c2[2] : c2[3];
	if (dist1 > dist2)
		return (1);
	else
		return (0);
}
